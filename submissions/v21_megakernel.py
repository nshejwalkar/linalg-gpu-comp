# Dispatch — megakernel where it wins (currently none), else v19's exact regimes.
# ══════════════════════════════════════════════════════════════════════════════
def _use_panel(batch: int, n: int) -> bool:
    return batch >= 32 and 128 <= n <= 1024


def custom_kernel(data: input_t) -> output_t:
    batch, n, _ = data.shape
    # Megakernel path (inert unless a shape is routed to it AND its cubin loaded).
    if (batch, n) in _MEGA_SHAPES:
        mega = _get_mega()
        if mega.ok:
            out = mega.run(data)
            if out is not None:
                return out
    # v19 fallback (default — no regression).
    if n < 128:
        return _fused_qr(data)
    if _use_panel(batch, n):
        return _blocked_wy_triton(data)
    return torch.geqrf(data)
